import SwiftUI

extension Color {

    static var theme: Color {
        return Color("Theme")
    }

    static var backgroundPrimary: Color {
        return Color("BackgroundPrimary")
    }

    static var textPrimary: Color {
        return Color("TextPrimary")
    }

    static var textDisabled: Color {
        return Color("TextDisabled")
    }

}
